    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed

        double numero1 = Double.parseDouble(txtnumerouno.getText());
        double numero2 = Double.parseDouble(txtnumerodos.getText());
        
        int opcion = cmbOperaciones.getSelectedIndex();
        if (opcion == 0) {
            double suma = sumar(numero1, numero2);
            txtResultado.setText(""+suma);
        }
        if (opcion == 1) {
            double resta  = resta(numero1, numero2);
            txtResultado.setText(""+resta);
        }
        if (opcion == 2) {
            double multiplicacion = multiplicacion(numero1, numero2);
            txtResultado.setText(""+multiplicacion);
        }
        if (opcion == 3) {
            double division = dividir(numero1, numero2);
            txtResultado.setText(""+division);
        }


    }//GEN-LAST:event_btnCalcularActionPerformed

    public static double sumar(double numerouno, double numerodos) {
        double suma = numerouno + numerodos;
        return suma;
    }

    public static double resta(double numerouno, double numerodos) {
        double resta = numerouno - numerodos;
        return resta;
    }

    public static double multiplicacion(double numerouno, double numerodos) {
        double multiplicacion = numerouno * numerodos;
        return multiplicacion;
    }

    public static double dividir(double numerouno, double numerodos) {
        if (numerodos == 0) {
            JOptionPane.showMessageDialog(null, "Division por cero no existe ");
        } else {
            double dividir = numerouno / numerodos;
            return dividir;
        }
        return 0;
    }

